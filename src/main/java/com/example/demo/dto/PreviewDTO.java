package com.example.demo.dto;

public abstract class PreviewDTO {
    Long id;
    String name;

    public PreviewDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
